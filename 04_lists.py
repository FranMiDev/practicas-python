# Using list built-in function
# Syntax
lst = list()

empty_list = list() # this is an empty list, no item in the list
print(len(empty_list)) # 0
print('-----------------------------------------------------')

# Using square brackets, []
# syntax
lst = []

empty_list = []
print(len(empty_list))
print('-----------------------------------------------------')

